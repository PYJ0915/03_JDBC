CREATE TABLE KH_STUDENT (
	STD_NO NUMBER PRIMARY KEY,
	STD_NAME NVARCHAR2(5) NOT NULL,
	STD_AGE NUMBER NOT NULL,
	MAJOR NVARCHAR2(10) NOT NULL,
	ENT_DATE DATE DEFAULT SYSDATE,
	CLUB_NO REFERENCES KH_CLUB
);

CREATE TABLE KH_CLUB (
	CLUB_NO NUMBER PRIMARY KEY,
	CLUB_NAME VARCHAR2(30) UNIQUE,
	CAPACITY NUMBER NOT NULL,
	EC_YN CHAR(1) CHECK(EC_YN IN ('Y','N')),
	ACTIVITY_TYPE VARCHAR2(100)
);

